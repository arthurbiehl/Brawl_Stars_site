function inicializarBonecos() {
    var container_bonecos = document.getElementById('browles');
    container_bonecos.innerHTML = '';

    items.forEach(val => {
        container_bonecos.innerHTML += `
            <div class='bonecos-single'>
                <img src="${val.img}" />
                <h2>${val.nome}</h2>
                <p>${val.sobre}</p>

            </div>
        `;
    });
}

function buscarItensPorNome() {
    const inputBusca = document.getElementById('inputBusca');
    const termoBusca = inputBusca.value.trim().toLowerCase();

    const itemsFiltrados = items.filter(item => item.nome.toLowerCase().includes(termoBusca));

    var container_bonecos = document.getElementById('browles');
    container_bonecos.innerHTML = ''; // Limpa o conteúdo anterior

    itemsFiltrados.forEach(val => {
        container_bonecos.innerHTML += `
            <div class='bonecos-single'>
                <img src="${val.img}" />
                <h2>${val.nome}</h2>
                <p>${val.sobre}</p>
            </div>
        `;
    });
}

document.addEventListener('DOMContentLoaded', function () {
    inicializarBonecos();
});